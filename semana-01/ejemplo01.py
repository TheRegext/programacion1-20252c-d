# hacer un programa que ingrese la nota de 3 examenes y muestre el promedio

# datos importantes

# Datos de entrada
# 3 notas
nota1 = int(input("Ingrese nota 1: ")) ## convertimos el dato que ingresa (que es un texto) a entero
nota2 = int(input("Ingrese nota 2: "))
nota3 = int(input("Ingrese nota 3: "))
# Operaciones
# calcular promedio

promedio = (nota1 + nota2 + nota3) / 3

# Datos de salida
# promedio
print(promedio)

